package com.day1.com;

public class DataTypes_learnigs {

	public static void main(String args[]) {

		System.out.println("************** Different Data Types *********************\n");
		
		boolean blnflag = true;
		System.out.println("Printing Value of Boolean =" + blnflag);

		char charCharectere = 'R';
		System.out.println("Printing Value of Char =" + charCharectere);

		byte bytB = 127;
		System.out.println("Printing Value of Byte =" + bytB);

		short shortNumber = 10000;
		System.out.println("Printing Value of short =" + shortNumber);

		int intNumber = 1000000000;
		System.out.println("Printing Value of integer =" + intNumber);

		long longNumber = 1000000000000000000L;
		System.out.println("Printing Value of longInteger =" + longNumber);

		float floatdecimalNumber = 542.4f;
		System.out.println("Printing Value of float =" + floatdecimalNumber);

		double dblDecimalNumber = 542.4;

		System.out.println("Printing Value of double =" + dblDecimalNumber);

		String strContry = "India";
		System.out.println("Printing Value of String =" + strContry);

	}

}
